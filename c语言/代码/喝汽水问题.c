#define _CRT_SECURE_NO_WARNINGS
#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

#include<stdlib.h>



int main()

{

	int money = 0;

	printf("请输入money=");

	scanf("%d", &money);

	int total = money;

	int empty = money;

	while (empty >= 2)

	{

		total += empty / 2;

		empty = empty / 2 + empty % 2;
		
	}

	printf("你可以喝 %d 瓶饮料\n", total);

	system("pause");

	return 0;

}